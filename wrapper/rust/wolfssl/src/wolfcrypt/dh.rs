    /// * `p_size`: Output parameter containing size of DH `p` parameter.
    /// * `g_size`: Output parameter containing size of DH `g` parameter.
    /// * `q_size`: Output parameter containing size of DH `q` parameter.
    ///
    /// # Example
    ///
    /// ```rust
