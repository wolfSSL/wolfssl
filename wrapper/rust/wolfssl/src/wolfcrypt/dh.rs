    /// # Parameters
    ///
    /// * `name`: DH parameters name, one of DH::FFDHE_*.
    ///
    /// # Returns
    ///
    /// Minimum key size for the DH named parameter set.
    ///
    /// # Example
    ///
    /// ```rust
    /// use wolfssl::wolfcrypt::random::RNG;
